// Package emit renders an ir.File to Go source. It writes to a buffer and runs
// go/format over the result, so the emit code can stay readable and let the
// formatter normalize whitespace.
package emit

import (
	"bytes"
	"fmt"
	"go/format"
	"strconv"
	"strings"
	"unicode"

	"github.com/binadel/esdigo/gen/ir"
)

// File renders the whole file, gofmt-normalized.
func File(f *ir.File) ([]byte, error) {
	var b bytes.Buffer

	b.WriteString("// Code generated by esdigo. DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package %s\n\n", f.Package)

	if len(f.Messages) > 0 && len(f.Imports) > 0 {
		emitImports(&b, f.Imports)
	}

	for _, m := range f.Messages {
		emitMessage(&b, m)
	}

	src, err := format.Source(b.Bytes())
	if err != nil {
		return nil, fmt.Errorf("format generated source: %w\n%s", err, b.Bytes())
	}
	return src, nil
}

// Files renders each message into its own gofmt-normalized source file, keyed by a
// snake_case filename derived from the type name (AssetResponse -> asset_response.go).
// Sibling types share the package, so each file carries only the imports its own
// message uses.
func Files(f *ir.File) (map[string][]byte, error) {
	files := make(map[string][]byte, len(f.Messages))
	for _, m := range f.Messages {
		var b bytes.Buffer

		b.WriteString("// Code generated by esdigo. DO NOT EDIT.\n\n")
		fmt.Fprintf(&b, "package %s\n\n", f.Package)

		if len(m.Imports) > 0 {
			emitImports(&b, m.Imports)
		}
		emitMessage(&b, m)

		src, err := format.Source(b.Bytes())
		if err != nil {
			return nil, fmt.Errorf("format generated source for %s: %w\n%s", m.Name, err, b.Bytes())
		}
		files[fileName(m.Name)] = src
	}
	return files, nil
}

// fileName is the snake_case source filename for a generated type
// (AssetListResponse -> asset_list_response.go).
func fileName(typeName string) string {
	var b strings.Builder
	for i, r := range typeName {
		if i > 0 && unicode.IsUpper(r) {
			b.WriteByte('_')
		}
		b.WriteRune(unicode.ToLower(r))
	}
	b.WriteString(".go")
	return b.String()
}

// emitImports renders the import block, keeping standard-library imports in a
// first group and external ones in a second (go/format sorts within a group but
// does not separate them). Input is assumed already sorted.
func emitImports(b *bytes.Buffer, imports []string) {
	var std, ext []string
	for _, imp := range imports {
		if isStdlib(imp) {
			std = append(std, imp)
		} else {
			ext = append(ext, imp)
		}
	}

	b.WriteString("import (\n")
	for _, imp := range std {
		fmt.Fprintf(b, "\t%q\n", imp)
	}
	if len(std) > 0 && len(ext) > 0 {
		b.WriteString("\n")
	}
	for _, imp := range ext {
		fmt.Fprintf(b, "\t%q\n", imp)
	}
	b.WriteString(")\n\n")
}

// isStdlib reports whether an import path is from the standard library — its
// first path segment carries no dot (no domain).
func isStdlib(path string) bool {
	first := path
	if i := strings.IndexByte(path, '/'); i >= 0 {
		first = path[:i]
	}
	return !strings.Contains(first, ".")
}

func emitMessage(b *bytes.Buffer, m *ir.Message) {
	if m.Union != nil {
		emitUnion(b, m)
		return
	}

	recv := receiverName(m.Name)

	// The model is always emitted in full (marshal, unmarshal, write, read) so a type
	// is usable as a nested field regardless of its x-esdigo-validate flag; the flag
	// gates only the validators, which a produce-only type does not need.
	emitStruct(b, m)
	emitMarshalJSON(b, m, recv)
	emitUnmarshalJSON(b, m, recv)
	emitWriteJSON(b, m, recv)
	emitReadJSON(b, m, recv)
	if m.Validated {
		emitValidator(b, m, recv)
	}
}

func emitStruct(b *bytes.Buffer, m *ir.Message) {
	if m.Doc != "" {
		fmt.Fprintf(b, "// %s %s\n", m.Name, m.Doc)
	}
	fmt.Fprintf(b, "type %s struct {\n", m.Name)
	for _, f := range m.Fields {
		if f.Doc != "" {
			fmt.Fprintf(b, "\t// %s %s\n", f.GoName, f.Doc)
		}
		fmt.Fprintf(b, "\t%s %s `json:%q`\n", f.GoName, f.ModelType, f.JSONName)
	}
	b.WriteString("}\n\n")
}

func emitMarshalJSON(b *bytes.Buffer, m *ir.Message, recv string) {
	fmt.Fprintf(b, "func (%s *%s) MarshalJSON() ([]byte, error) {\n", recv, m.Name)
	b.WriteString("\tw := json.NewWriter(128)\n")
	fmt.Fprintf(b, "\t%s.WriteJSON(w)\n", recv)
	b.WriteString("\treturn w.Build()\n}\n\n")
}

func emitUnmarshalJSON(b *bytes.Buffer, m *ir.Message, recv string) {
	fmt.Fprintf(b, "func (%s *%s) UnmarshalJSON(data []byte) error {\n", recv, m.Name)
	b.WriteString("\tr := json.NewReader(data)\n")
	fmt.Fprintf(b, "\t%s.ReadJSON(r)\n", recv)
	b.WriteString("\treturn r.Error()\n}\n\n")
}

func emitWriteJSON(b *bytes.Buffer, m *ir.Message, recv string) {
	fmt.Fprintf(b, "func (%s *%s) WriteJSON(w *json.Writer) bool {\n", recv, m.Name)
	fmt.Fprintf(b, "\tif %s == nil {\n\t\tw.WriteNull()\n\t\treturn true\n\t}\n", recv)
	if len(m.Fields) > 0 {
		b.WriteString("\tneedsComma := false\n")
	}
	b.WriteString("\tw.BeginObject()\n")
	for _, f := range m.Fields {
		fmt.Fprintf(b, "\tif %s.%s.IsPresent() {\n", recv, f.GoName)
		b.WriteString("\t\tif needsComma {\n\t\t\tw.ValueSeparator()\n\t\t}\n")
		fmt.Fprintf(b, "\t\tw.WriteRawString(%q)\n", `"`+f.JSONName+`":`)
		fmt.Fprintf(b, "\t\tif !%s.%s.WriteJSON(w) {\n\t\t\treturn false\n\t\t}\n", recv, f.GoName)
		b.WriteString("\t\tneedsComma = true\n\t}\n")
	}
	b.WriteString("\tw.EndObject()\n\treturn true\n}\n\n")
}

func emitReadJSON(b *bytes.Buffer, m *ir.Message, recv string) {
	fmt.Fprintf(b, "func (%s *%s) ReadJSON(r *json.Reader) bool {\n", recv, m.Name)
	b.WriteString("\tr.SkipWhitespace()\n")
	b.WriteString("\tif r.BeginObject() {\n")
	b.WriteString("\t\tr.SkipWhitespace()\n")
	b.WriteString("\t\tif r.EndObject() {\n\t\t\treturn true\n\t\t}\n")
	b.WriteString("\t\tfor {\n")
	b.WriteString("\t\t\tif name, ok := r.ReadString(); ok {\n")
	b.WriteString("\t\t\t\tr.SkipWhitespace()\n")
	b.WriteString("\t\t\t\tif r.NameSeparator() {\n")
	b.WriteString("\t\t\t\t\tok := false\n")
	b.WriteString("\t\t\t\t\tswitch name {\n")
	for _, f := range m.Fields {
		fmt.Fprintf(b, "\t\t\t\t\tcase %q:\n", f.JSONName)
		fmt.Fprintf(b, "\t\t\t\t\t\tok = %s.%s.ReadJSON(r)\n", recv, f.GoName)
	}
	b.WriteString("\t\t\t\t\tdefault:\n\t\t\t\t\t\tok = r.SkipValue()\n")
	b.WriteString("\t\t\t\t\t}\n")
	b.WriteString("\t\t\t\t\tif !ok {\n\t\t\t\t\t\treturn false\n\t\t\t\t\t}\n")
	b.WriteString("\t\t\t\t} else {\n")
	b.WriteString("\t\t\t\t\tr.SetSyntaxError(\"expected a name-separator ':' after name\")\n")
	b.WriteString("\t\t\t\t\treturn false\n\t\t\t\t}\n")
	b.WriteString("\t\t\t\tr.SkipWhitespace()\n")
	b.WriteString("\t\t\t\tif r.EndObject() {\n\t\t\t\t\treturn true\n\t\t\t\t}\n")
	b.WriteString("\t\t\t\tif !r.ValueSeparator() {\n")
	b.WriteString("\t\t\t\t\tr.SetSyntaxError(\"expected either end-object '}' or value-separator ','\")\n")
	b.WriteString("\t\t\t\t\treturn false\n\t\t\t\t}\n")
	b.WriteString("\t\t\t\tr.SkipWhitespace()\n")
	b.WriteString("\t\t\t} else {\n")
	b.WriteString("\t\t\t\tr.SetSyntaxError(\"expected a name after begin-object '{' or value-separator ','\")\n")
	b.WriteString("\t\t\t\treturn false\n\t\t\t}\n")
	b.WriteString("\t\t}\n")
	b.WriteString("\t}\n")
	b.WriteString("\treturn false\n}\n\n")
}

// emitValidator emits the validation trio plus IsValid/Collect/Errors. Object
// fields recurse: the validated result nests *Validated<Child>, the validator
// holds the child validator and an object-level checker, and validation descends
// into the child. Every Validated struct carries an object-level Object result and
// its field validators take a base path so nested errors get full paths.
func emitValidator(b *bytes.Buffer, m *ir.Message, recv string) {
	arg := recv
	if arg == "v" { // avoid clashing with the validator receiver in Validate
		arg = "src"
	}

	// Validated<Message>
	fmt.Fprintf(b, "type Validated%s struct {\n", m.Name)
	fmt.Fprintf(b, "\tObject validation.Result[*%s]\n", m.Name)
	if hasPropertyCount(m) {
		b.WriteString("\tProperties validation.Result[int]\n")
	}
	for _, f := range m.Fields {
		if !f.Validate {
			continue
		}
		if f.IsObject {
			fmt.Fprintf(b, "\t%s *Validated%s\n", f.GoName, f.ChildType)
			continue
		}
		fmt.Fprintf(b, "\t%s validation.Result[%s]\n", f.GoName, f.ResultType)
		if f.ElemValidate {
			fmt.Fprintf(b, "\t%sItems []%s\n", f.GoName, f.ElemItemsType)
		}
	}
	b.WriteString("}\n\n")

	// <Message>Validator
	fmt.Fprintf(b, "type %sValidator struct {\n", m.Name)
	for _, f := range m.Fields {
		if !f.Validate {
			continue
		}
		if f.IsObject {
			fmt.Fprintf(b, "\t%s *%sValidator\n", f.GoName, f.ChildType)
			fmt.Fprintf(b, "\t%s *validation.Object[%s, *%s]\n", objField(f.GoName), f.ChildType, f.ChildType)
			continue
		}
		fmt.Fprintf(b, "\t%s %s\n", f.GoName, f.ValidatorType)
	}
	if hasPropertyCount(m) {
		b.WriteString("\tproperties *validation.Properties\n")
	}
	if hasElemValidate(m) {
		b.WriteString("\tbase []string\n")
	}
	b.WriteString("}\n\n")

	// New<Message>Validator(base ...string)
	fmt.Fprintf(b, "func New%sValidator(base ...string) *%sValidator {\n", m.Name, m.Name)
	fmt.Fprintf(b, "\treturn &%sValidator{\n", m.Name)
	for _, f := range m.Fields {
		if !f.Validate {
			continue
		}
		if f.IsObject {
			fmt.Fprintf(b, "\t\t%s: %s,\n", f.GoName, f.ChildNewExpr)
			fmt.Fprintf(b, "\t\t%s: %s,\n", objField(f.GoName), f.ObjectNewExpr)
			continue
		}
		fmt.Fprintf(b, "\t\t%s: %s,\n", f.GoName, f.NewExpr)
	}
	if hasPropertyCount(m) {
		fmt.Fprintf(b, "\t\tproperties: %s,\n", propertiesNewExpr(m))
	}
	if hasElemValidate(m) {
		b.WriteString("\t\tbase: base,\n")
	}
	b.WriteString("\t}\n}\n\n")

	// Validate(x *X) *ValidatedX
	fmt.Fprintf(b, "func (v *%sValidator) Validate(%s *%s) *Validated%s {\n", m.Name, arg, m.Name, m.Name)
	fmt.Fprintf(b, "\tif %s == nil {\n\t\treturn &Validated%s{}\n\t}\n", arg, m.Name)
	fmt.Fprintf(b, "\tout := &Validated%s{}\n", m.Name)
	for _, f := range m.Fields {
		if !f.Validate {
			continue
		}
		if f.IsObject {
			fmt.Fprintf(b, "\tout.%s = v.%s.Validate(%s.%s.Value)\n", f.GoName, f.GoName, arg, f.GoName)
			fmt.Fprintf(b, "\tout.%s.Object = v.%s.Validate(%s.%s)\n", f.GoName, objField(f.GoName), arg, f.GoName)
			continue
		}
		expr := arg + "." + f.GoName
		if f.ByPointer {
			expr = "&" + expr
		}
		fmt.Fprintf(b, "\tout.%s = v.%s.Validate(%s)\n", f.GoName, f.GoName, expr)
		if f.ElemValidate {
			elemArg := "elem"
			if f.ElemByValue {
				elemArg = "*elem"
			}
			fmt.Fprintf(b, "\tfor i, elem := range %s.%s.Value {\n", arg, f.GoName)
			fmt.Fprintf(b, "\t\tp := validation.SubPath(validation.SubPath(v.base, %q), strconv.Itoa(i))\n", f.JSONName)
			fmt.Fprintf(b, "\t\tout.%sItems = append(out.%sItems, %s.Validate(%s))\n", f.GoName, f.GoName, f.ElemNewExpr, elemArg)
			b.WriteString("\t}\n")
		}
	}
	if hasPropertyCount(m) {
		b.WriteString("\tcount := 0\n")
		for _, f := range m.Fields {
			fmt.Fprintf(b, "\tif %s.%s.IsPresent() {\n\t\tcount++\n\t}\n", arg, f.GoName)
		}
		b.WriteString("\tout.Properties = v.properties.Validate(count)\n")
	}
	b.WriteString("\treturn out\n}\n\n")

	// IsValid: object-level plus every field, recursing into child objects and
	// array elements.
	fmt.Fprintf(b, "func (v *Validated%s) IsValid() bool {\n", m.Name)
	b.WriteString("\tif !v.Object.IsValid() {\n\t\treturn false\n\t}\n")
	if hasPropertyCount(m) {
		b.WriteString("\tif !v.Properties.IsValid() {\n\t\treturn false\n\t}\n")
	}
	for _, f := range m.Fields {
		if !f.Validate {
			continue
		}
		if f.IsObject {
			fmt.Fprintf(b, "\tif v.%s != nil && !v.%s.IsValid() {\n\t\treturn false\n\t}\n", f.GoName, f.GoName)
			continue
		}
		fmt.Fprintf(b, "\tif !v.%s.IsValid() {\n\t\treturn false\n\t}\n", f.GoName)
		if f.ElemValidate {
			fmt.Fprintf(b, "\tfor i := range v.%sItems {\n\t\tif !v.%sItems[i].IsValid() {\n\t\t\treturn false\n\t\t}\n\t}\n", f.GoName, f.GoName)
		}
	}
	b.WriteString("\treturn true\n}\n\n")

	// Collect: gather every failing field result (each carries its full path) into
	// out, descending into child objects.
	fmt.Fprintf(b, "func (v *Validated%s) Collect(out *[]validation.FieldResult) {\n", m.Name)
	b.WriteString("\tif !v.Object.IsValid() {\n\t\t*out = append(*out, &v.Object)\n\t}\n")
	if hasPropertyCount(m) {
		b.WriteString("\tif !v.Properties.IsValid() {\n\t\t*out = append(*out, &v.Properties)\n\t}\n")
	}
	for _, f := range m.Fields {
		if !f.Validate {
			continue
		}
		if f.IsObject {
			fmt.Fprintf(b, "\tif v.%s != nil {\n\t\tv.%s.Collect(out)\n\t}\n", f.GoName, f.GoName)
			continue
		}
		fmt.Fprintf(b, "\tif !v.%s.IsValid() {\n\t\t*out = append(*out, &v.%s)\n\t}\n", f.GoName, f.GoName)
		if f.ElemValidate {
			if f.ElemIsObject {
				fmt.Fprintf(b, "\tfor i := range v.%sItems {\n\t\tv.%sItems[i].Collect(out)\n\t}\n", f.GoName, f.GoName)
			} else {
				fmt.Fprintf(b, "\tfor i := range v.%sItems {\n\t\tif !v.%sItems[i].IsValid() {\n\t\t\t*out = append(*out, &v.%sItems[i])\n\t\t}\n\t}\n", f.GoName, f.GoName, f.GoName)
			}
		}
	}
	b.WriteString("}\n\n")

	// Failures: the flat list of failing field results (path + codes) across the tree.
	fmt.Fprintf(b, "func (v *Validated%s) Failures() []validation.FieldResult {\n", m.Name)
	b.WriteString("\tvar out []validation.FieldResult\n\tv.Collect(&out)\n\treturn out\n}\n\n")
}

// emitUnion renders a discriminated-union message: a struct with one pointer per
// variant (exactly one set), a read that peeks the discriminator and decodes the
// matching variant, a write that delegates to the set variant, and a validator that
// recurses into whichever variant is present. Marshal/Unmarshal are the shared
// wrappers. A parent references the union as an ordinary object field, so its
// validator API mirrors a struct message's.
func emitUnion(b *bytes.Buffer, m *ir.Message) {
	recv := receiverName(m.Name)

	// Like emitMessage: the model is always emitted in full; the x-esdigo-validate
	// flag gates only the validators.
	emitUnionStruct(b, m)
	emitMarshalJSON(b, m, recv)
	emitUnmarshalJSON(b, m, recv)
	emitUnionWriteJSON(b, m, recv)
	emitUnionReadJSON(b, m, recv)
	if m.Validated {
		emitUnionValidator(b, m, recv)
	}
}

func emitUnionStruct(b *bytes.Buffer, m *ir.Message) {
	if m.Doc != "" {
		fmt.Fprintf(b, "// %s %s\n", m.Name, m.Doc)
	} else {
		fmt.Fprintf(b, "// %s is a discriminated union.\n", m.Name)
	}
	fmt.Fprintf(b, "// Exactly one field is set, selected by the %q property.\n", m.Union.Discriminator)
	fmt.Fprintf(b, "type %s struct {\n", m.Name)
	for _, v := range m.Union.Variants {
		fmt.Fprintf(b, "\t%s *%s\n", v.GoType, v.GoType)
	}
	b.WriteString("}\n\n")
}

func emitUnionWriteJSON(b *bytes.Buffer, m *ir.Message, recv string) {
	fmt.Fprintf(b, "func (%s *%s) WriteJSON(w *json.Writer) bool {\n", recv, m.Name)
	fmt.Fprintf(b, "\tif %s == nil {\n\t\tw.WriteNull()\n\t\treturn true\n\t}\n", recv)
	b.WriteString("\tswitch {\n")
	for _, v := range m.Union.Variants {
		fmt.Fprintf(b, "\tcase %s.%s != nil:\n\t\treturn %s.%s.WriteJSON(w)\n", recv, v.GoType, recv, v.GoType)
	}
	b.WriteString("\t}\n")
	b.WriteString("\tw.WriteNull()\n\treturn true\n}\n\n")
}

func emitUnionReadJSON(b *bytes.Buffer, m *ir.Message, recv string) {
	disc := m.Union.Discriminator
	fmt.Fprintf(b, "func (%s *%s) ReadJSON(r *json.Reader) bool {\n", recv, m.Name)
	fmt.Fprintf(b, "\t*%s = %s{}\n", recv, m.Name)
	// Capture the raw object so the discriminator can be inspected before choosing a
	// variant, then hand the same bytes to that variant's reader.
	b.WriteString("\traw, ok := r.ReadRawValue()\n\tif !ok {\n\t\treturn false\n\t}\n")
	b.WriteString("\tsub := json.NewReader(raw)\n")
	b.WriteString("\tif sub.ReadNull() {\n\t\treturn true\n\t}\n")
	b.WriteString("\tobj, ok := sub.ReadObject()\n\tif !ok {\n\t\t")
	emitSetError(b, m.Name+": a oneOf value must be an object")
	b.WriteString("\t\treturn false\n\t}\n")
	fmt.Fprintf(b, "\ttag, ok := obj[%q]\n", disc)
	b.WriteString("\tif !ok || tag.Type != json.ValueTypeString {\n\t\t")
	emitSetError(b, m.Name+": missing string discriminator %q", strconv.Quote(disc))
	b.WriteString("\t\treturn false\n\t}\n")
	b.WriteString("\tvalue, _ := tag.Payload.(string)\n")
	b.WriteString("\tswitch value {\n")
	for _, v := range m.Union.Variants {
		fmt.Fprintf(b, "\tcase %q:\n", v.Tag)
		fmt.Fprintf(b, "\t\t%s.%s = new(%s)\n", recv, v.GoType, v.GoType)
		fmt.Fprintf(b, "\t\treturn %s.%s.ReadJSON(json.NewReader(raw))\n", recv, v.GoType)
	}
	b.WriteString("\t}\n\t")
	emitSetError(b, m.Name+": unknown discriminator value %q", "value")
	b.WriteString("\treturn false\n}\n\n")
}

// emitSetError writes an r.SetSyntaxError call. format is emitted as a Go string
// literal (so any %q verbs are preserved) and args are Go expressions passed through.
func emitSetError(b *bytes.Buffer, format string, args ...string) {
	fmt.Fprintf(b, "r.SetSyntaxError(%s", strconv.Quote(format))
	for _, a := range args {
		fmt.Fprintf(b, ", %s", a)
	}
	b.WriteString(")\n")
}

// emitUnionValidator mirrors the struct-message validator API (Validated<T>,
// <T>Validator, New<T>Validator, Validate, IsValid, Collect, Failures) so a parent
// can validate a union field exactly like a nested object: the object-level presence
// result is set by the parent, and validation recurses into the present variant.
func emitUnionValidator(b *bytes.Buffer, m *ir.Message, recv string) {
	arg := recv
	if arg == "v" { // avoid clashing with the validator receiver in Validate
		arg = "src"
	}

	fmt.Fprintf(b, "type Validated%s struct {\n", m.Name)
	fmt.Fprintf(b, "\tObject validation.Result[*%s]\n", m.Name)
	for _, v := range m.Union.Variants {
		fmt.Fprintf(b, "\t%s *Validated%s\n", v.GoType, v.GoType)
	}
	b.WriteString("}\n\n")

	fmt.Fprintf(b, "type %sValidator struct {\n", m.Name)
	for _, v := range m.Union.Variants {
		fmt.Fprintf(b, "\t%s *%sValidator\n", v.GoType, v.GoType)
	}
	b.WriteString("}\n\n")

	fmt.Fprintf(b, "func New%sValidator(base ...string) *%sValidator {\n", m.Name, m.Name)
	fmt.Fprintf(b, "\treturn &%sValidator{\n", m.Name)
	for _, v := range m.Union.Variants {
		fmt.Fprintf(b, "\t\t%s: New%sValidator(base...),\n", v.GoType, v.GoType)
	}
	b.WriteString("\t}\n}\n\n")

	fmt.Fprintf(b, "func (v *%sValidator) Validate(%s *%s) *Validated%s {\n", m.Name, arg, m.Name, m.Name)
	fmt.Fprintf(b, "\tout := &Validated%s{}\n", m.Name)
	fmt.Fprintf(b, "\tif %s == nil {\n\t\treturn out\n\t}\n", arg)
	for _, v := range m.Union.Variants {
		fmt.Fprintf(b, "\tif %s.%s != nil {\n\t\tout.%s = v.%s.Validate(%s.%s)\n\t}\n", arg, v.GoType, v.GoType, v.GoType, arg, v.GoType)
	}
	b.WriteString("\treturn out\n}\n\n")

	fmt.Fprintf(b, "func (v *Validated%s) IsValid() bool {\n", m.Name)
	b.WriteString("\tif !v.Object.IsValid() {\n\t\treturn false\n\t}\n")
	for _, v := range m.Union.Variants {
		fmt.Fprintf(b, "\tif v.%s != nil && !v.%s.IsValid() {\n\t\treturn false\n\t}\n", v.GoType, v.GoType)
	}
	b.WriteString("\treturn true\n}\n\n")

	fmt.Fprintf(b, "func (v *Validated%s) Collect(out *[]validation.FieldResult) {\n", m.Name)
	b.WriteString("\tif !v.Object.IsValid() {\n\t\t*out = append(*out, &v.Object)\n\t}\n")
	for _, v := range m.Union.Variants {
		fmt.Fprintf(b, "\tif v.%s != nil {\n\t\tv.%s.Collect(out)\n\t}\n", v.GoType, v.GoType)
	}
	b.WriteString("}\n\n")

	fmt.Fprintf(b, "func (v *Validated%s) Failures() []validation.FieldResult {\n", m.Name)
	b.WriteString("\tvar out []validation.FieldResult\n\tv.Collect(&out)\n\treturn out\n}\n\n")
}

// receiverName is the method receiver for a generated type: the lowercased first
// letter, as is idiomatic. The only single-letter identifiers in the generated
// method bodies are the fixed json.Reader "r" and json.Writer "w" parameters, so a
// type whose name starts with R or W would shadow them (e.g. func (r *Report)
// ReadJSON(r *json.Reader)). In that case fall back to the first two letters, which
// cannot collide with those single-letter params or any multi-letter local.
func receiverName(typeName string) string {
	recv := strings.ToLower(typeName[:1])
	if recv != "r" && recv != "w" {
		return recv
	}
	if len(typeName) >= 2 {
		return strings.ToLower(typeName[:2])
	}
	return recv + recv
}

// objField is the unexported object-level checker field name for an object field,
// e.g. "customerObject" for GoName "Customer".
func objField(goName string) string {
	return strings.ToLower(goName[:1]) + goName[1:] + "Object"
}

// hasPropertyCount reports whether the message carries a minProperties/maxProperties
// constraint, so the validator gains a property-count checker.
func hasPropertyCount(m *ir.Message) bool {
	return m.MinProperties != nil || m.MaxProperties != nil
}

// propertiesNewExpr renders the property-count validator constructor at the object's
// own base path, with the Min/Max chain the message requires.
func propertiesNewExpr(m *ir.Message) string {
	var b strings.Builder
	b.WriteString("validation.NewProperties(base...)")
	if m.MinProperties != nil {
		fmt.Fprintf(&b, ".Min(%d)", *m.MinProperties)
	}
	if m.MaxProperties != nil {
		fmt.Fprintf(&b, ".Max(%d)", *m.MaxProperties)
	}
	return b.String()
}

// hasElemValidate reports whether any field validates per element, which means the
// validator retains its base path to build the indexed element validators.
func hasElemValidate(m *ir.Message) bool {
	for _, f := range m.Fields {
		if f.ElemValidate {
			return true
		}
	}
	return false
}
